    private void validarCampos(){
        if(txtCarroceria.equals("")){
            JOptionPane.showMessageDialog(null,"EL CAMPO CARROCERIA ESTA VACIO");
            txtCarroceria.requestFocus();
        }else{
            if(txtDescripcion.equals("")){
                JOptionPane.showMessageDialog(null,"EL CAMPO DESCRIPCION ESTA VACIO");
                txtDescripcion.requestFocus();
            }else{
                if(txtMarcaMotor.equals("")){
                    JOptionPane.showMessageDialog(null,"EL CAMPO MARCA MOTOR ESTA VACIO");
                }else{
                    validar=50;
                    cMarca.pasarDatos(mMarca);
                    cMarca.insertarMarca(mMarca);
                }
            }
        }
    }
